    /**
     * @param Request $request
     * @param ImageUploadHandler $uploader
     * @return mixed
     */
    public function store(Request $request, ImageUploadHandler $uploader)
    {
        try {
            $img = $request->img;
            $result = $uploader->save($img, 'other', 't');

            Img::create([
                'url' => $result['path']
            ]);

            return $this->response->array($result);
        } catch (\Exception $e) {
            return $e;
        }
    }

